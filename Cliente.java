package exerciciospoo;

public class Cliente {

		
		String nome;
		String telefone;
		String cpf;
		String rg;
		String comprar;
		String reclama;
		String andar;
		String negocia;
		
		
		public String getNome() {
			return nome;
		}
		public void setNome(String nome) {
			this.nome = nome;
		}
		public String getTelefone() {
			return telefone;
		}
		public void setTelefone (String telefone) {
			this.telefone = telefone;
		}
		public String getCpf() {
			return cpf;
		}
		public void setCpf(String cpf) {
			this.cpf = cpf;
		}
		public String getRg() {
			return rg;
		}
		public void setRg(String rg) {
			this.rg = rg;
		}
		public String getComprar() {
			return comprar;
		}
		public void setComprar(String comprar) {
			this.comprar = comprar;
		}
		public String getReclama() {
			return reclama;
		}
		public void setReclama(String reclama) {
			this.reclama = reclama;
		}
		public String getAndar() {
			return andar;
		}
		public void setAndar(String andar) {
			this.andar = andar;
		}
		public String getNegocia() {
			return negocia;
		}
		public void setNegocia(String negocia) {
			this.negocia = negocia;
		}
		
		

}
