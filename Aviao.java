package exerciciospoo;

public class Aviao {

	String aviadora;
	String cor;
	String funcao;
	String banheiro;
	String planar;
	String aterrisar;
	String decolar;
	String andarRodinha;

	public String getAviadora() {
		return aviadora;
	}
	public void setAviadora(String aviadora) {
		this.aviadora = aviadora;
	}
	public String getCor() {
		return cor;
	}
	public void setCor(String cor) {
		this.cor = cor;
	}
	public String getFuncao() {
		return funcao;
	}
	public void setFuncao(String funcao) {
		this.funcao = funcao;
	}
	public String getBanheiro() {
		return banheiro;
	}
	public void setBanheiro(String banheiro) {
		this.banheiro = banheiro;
	}
	public String getPlanar() {
		return planar;
	}
	public void setPlanar(String planar) {
		this.planar = planar;
	}
	public String getAterrisar() {
		return aterrisar;
	}
	public void setAterrisar(String aterrisar) {
		this.aterrisar = aterrisar;
	}
	public String getDecolar() {
		return decolar;
	}
	public void setDecolar(String decolar) {
		this.decolar = decolar;
	}
	public String getAndarRodinha() {
		return andarRodinha;
	}
	public void setAndarRodinha(String andarRodinha) {
		this.andarRodinha = andarRodinha;
	}
	
}


	


