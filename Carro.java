package exerciciospoo;

public class Carro {

	String name;
	String cor;
	String andar;
	String vireDireita;
	String vireEsquerda;
	String darSeta;
	 

	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getCor() {
		return cor;
	}
	public void setCor(String cor) {
		this.cor = cor;
	}
	public String getAndar() {
		return andar;
	}
	public void setAndar(String andar) {
		this.andar = andar;
	}
	public String getVireDireita() {
		return vireDireita;
	}
	public void setVireDireita(String vireDireita) {
		this.vireDireita = vireDireita;
	}
	public String getVireEsquerda() {
		return vireEsquerda;
	}
	public void setVireEsquerda(String vireEsquerda) {
		this.vireEsquerda = vireEsquerda;
	
	}
	public String getDarSeta() {
		return darSeta;
	}
	public void setDarSeta(String darSeta) {
		this.darSeta = darSeta;
	}

}





