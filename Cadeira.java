package exerciciospoo;

public class Cadeira {

	String modelo;
	String marca;
	String tipo;
	String cor;
	String anda;
	String gira;
	String sobe;
	String desce;

	public String getModelo() {
		return modelo;
	}
	public void setModelo(String modelo) {
		this.modelo = modelo;
	}
	public String getMarca() {
		return marca;
	}
	public void setMarca(String marca) {
		this.marca = marca; 
	}
	public String getTipo() {
		return tipo;
	}
	public void setTipo(String tipo) {
		this.tipo = tipo;
	}
	public String getCor() {
		return cor;
	}
	public void setCor(String cor) {
		this.cor = cor; 
	}
	public String getAnda() {
		return anda;
	}
	public void setAnda(String anda) {
		this.anda = anda;
	}
	public String getGira() {
		return gira;
	}
	public void setGira(String gira) {
		this.gira = gira; 
	}
	public String getSobe() {
		return sobe;
	}
	public void setSobe(String sobe) {
		this.sobe = sobe;
	}
	public String getDesce() {
		return desce;
	}
	public void setDesce(String desce) {
		this.desce = desce;
	}
	



	}


